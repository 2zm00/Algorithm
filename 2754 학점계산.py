
# 성적과 평점을 매핑하는 딕셔너리
gpa = {
    'A+': 4.3, 'A0': 4.0, 'A-': 3.7,
    'B+': 3.3, 'B0': 3.0, 'B-': 2.7,
    'C+': 2.3, 'C0': 2.0, 'C-': 1.7,
    'D+': 1.3, 'D0': 1.0, 'D-': 0.7,
    'F': 0.0
}

# 사용자로부터 입력 받기
grade = input()

# 평점 계산 및 출력
if grade in gpa:
    print(f"{gpa[grade]:.1f}")
else:
    print("올바르지 않은 성적입니다.")